number1 = 10
number2 = 5

# Addition
sum_result = number1 + number2
print(f"Sum of {number1} and {number2} = {sum_result}")

# Subtraction
subtraction_result = number1 - number2
print(f"Subtraction of {number1} and {number2} = {subtraction_result}")

# Multiplication
multiplication_result = number1 * number2
print(f"Multiplication of {number1} and {number2} = {multiplication_result}")
